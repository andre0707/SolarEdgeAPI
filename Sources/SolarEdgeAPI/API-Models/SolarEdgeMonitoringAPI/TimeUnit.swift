import Foundation

/// All the avaialble time units
public enum TimeUnit: String {
    case quarterOfAnHour = "QUARTER_OF_AN_HOUR"
    case hour = "HOUR"
    case day = "DAY"
    case week = "WEEK"
    case month = "MONTH"
    case year = "YEAR"
    
    case unknown = "UNKNOWN"
}
